void bubbleSort(int arr[], int SIZE)
{
    int i, j;
    for (i=0; i<SIZE-1; i++)
        for (j=0; j<SIZE-i-1; j++)
            if (arr[j]>arr[j+1])
                swap(&arr[j], &arr[j+1]);
                
    for (i=0; i<SIZE; i++)
    {
        if (i==0)
        {
            printf("Random Value Array : {");
            printf(" %d,", arr[i]);
        }
            
        else if (i==SIZE-1)
        {
            printf(" %d }\n", arr[i]);
        }
        
        else
            printf(" %d,", arr[i]);
    }
    
    for (i=0; i<SIZE; i++)
        array_with_length[i] = arr[i];
}

void swap(int *a, int *b)
{
    int temp = *a;
    *a = *b;
    *b = temp;
}
